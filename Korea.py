def Rectangle(size):
    color("black")
    begin_fill()
    t.forward(size)
    t.left(90)
    t.forward(20)
    t.left(90)
    t.forward(size*2)
    t.left(90)
    t.forward(20)
    t.left(90)
    t.forward(size)
    end_fill()

def Cut(size):
    color("white")
    begin_fill()
    t.forward(size)
    t.left(90)
    t.forward(23)
    t.left(90)
    t.forward(size)
    t.left(90)
    t.forward(23)
    t.left(90)
    t.forward(size)
    end_fill()

